import 'package:flutter/material.dart';

/// A class that stores the theme data
/// for the metrics table loading placeholder.
class MetricsTableLoadingPlaceholderThemeData {
  /// A background [Color] of the loading placeholder.
  final Color backgroundColor;

  /// A [Color] of the shimmer animation.
  final Color shimmerColor;

  /// Creates an instance of the [MetricsTableLoadingPlaceholderThemeData].
  const MetricsTableLoadingPlaceholderThemeData({
    this.backgroundColor,
    this.shimmerColor,
  });
}
